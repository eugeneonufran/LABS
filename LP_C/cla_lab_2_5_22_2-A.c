#include <stdio.h>
int main()
{
    float Value_A;
    printf("Value A: \n");
    scanf("%f", &Value_A);

    float Value_B;
    printf("Value_B: \n");
    scanf("%f", &Value_B);

    printf("Value A + Value B =  %f\n", Value_A + Value_B);
    printf("Value A - Value B =  %f\n", Value_A - Value_B);
    printf("Value A * Value B =  %f\n", Value_A * Value_B);

return 0;
}
